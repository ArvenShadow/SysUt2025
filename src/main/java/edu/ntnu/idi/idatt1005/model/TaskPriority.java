package edu.ntnu.idi.idatt1005.model;

/**
 * Represents the priority levels for tasks in the application.
 * Priority levels help categorize and sort tasks by their importance.
 */
public enum TaskPriority {
  /**
   * Low priority tasks that are less urgent.
   */
  LOW,

  /**
   * Medium priority tasks with moderate urgency.
   */
  MEDIUM,

  /**
   * High priority tasks requiring immediate attention.
   */
  HIGH
}